    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:

        Integer IDPhoneXPerson = Integer.parseInt(IDPhoneXPersonTextField.getText());
        Integer IDPerson;
        Integer IDPhone;

        if(IDPersonTextField.getText().isEmpty()){
            IDPerson = null;
        }else{
            IDPerson = Integer.parseInt(IDPersonTextField.getText());
        }

        if(IDPhoneTextField.getText().isEmpty()){
            IDPhone = null;
        }else{
            IDPhone = Integer.parseInt(IDPhoneTextField.getText());
        }

        getElements.updatePersonXPhone(IDPhoneXPerson, IDPerson, IDPhone);
    }//GEN-LAST:event_AddButtonActionPerformed

